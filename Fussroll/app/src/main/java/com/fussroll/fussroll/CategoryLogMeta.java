    static String[] getMetaForPlace(String place) {
        switch (place) {
            case "home":
                return new String[]{"At", "Away from", "Coming back"};
            case "friend's house":
            case "relative's house":
                return new String[]{"Going to my", "At my", "Left my"};
            case "beach":
                return new String[]{"Going to the", "At the", "Left the"};
            default:
                return new String[]{"Going to the", "In the", "Left the"};
        }
    }

